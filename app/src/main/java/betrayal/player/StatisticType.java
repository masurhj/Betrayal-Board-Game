package betrayal.player;

public enum StatisticType {
    /**
     * Indicates the player's might statistic, used in combat.
     */
    MIGHT,

    /**
     * Indicates the player's knowledge statistic, used in (fill me in).
     */
    KNOWLEDGE,

    /**
     * Indicates the player's speed statistic, used in movement.
     */
    SPEED,

    /**
     * Indicates the player's sanity statistic, used in (fill me in).
     */
    SANITY,

    /**
     * Replacement for null statistic type.
     */
    NONE,

    /**
     * Invalid statistic type.
     */
    INVALID
}
